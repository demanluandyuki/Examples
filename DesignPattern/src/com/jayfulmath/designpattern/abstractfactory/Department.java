package com.jayfulmath.designpattern.abstractfactory;

public class Department {
	private int _id;
	private String _name;

	
	public int getID() {
		return _id;
	}
	public void setID(int _id) {
		this._id = _id;
	}
	public String getName() {
		return _name;
	}
	public void setName(String _name) {
		this._name = _name;
	}
}
